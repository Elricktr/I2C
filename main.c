#include <xc.h>
#include <stdint.h>
#include <stdbool.h>

// CONFIGURATION BITS
#pragma config FOSC = INTRC_NOCLKOUT // Internal RC oscillator
#pragma config WDTE = OFF            // Watchdog Timer disabled
#pragma config PWRTE = OFF           // Power-up Timer disabled
#pragma config MCLRE = ON            // MCLR pin enabled
#pragma config CP = OFF              // Code Protection off
#pragma config CPD = OFF             // Data Code Protection off
#pragma config BOREN = ON            // Brown-out Reset enabled
#pragma config IESO = OFF            // Internal/External Switchover disabled
#pragma config FCMEN = OFF           // Fail-Safe Clock Monitor disabled
#pragma config LVP = OFF             // Low-Voltage Programming disabled

#define _XTAL_FREQ 8000000UL         // 8MHz internal oscillator

// I2C (MSSP) definitions
#define I2C_BAUD_RATE 100000         // 100kHz
#define SSPADD_VAL ((_XTAL_FREQ/(4UL*I2C_BAUD_RATE))-1)

// SSD1306 I2C address
#define SSD1306_ADDR 0x78            // 0x3C << 1 for write

// Control bytes
#define SSD1306_CMD  0x00
#define SSD1306_DATA 0x40

// Function prototypes
void I2C_Init(void);
void I2C_Start(void);
void I2C_Stop(void);
bool I2C_Write(uint8_t data);

void SSD1306_Command(uint8_t cmd);
void SSD1306_Data(uint8_t data);
void SSD1306_Init(void);
void SSD1306_Clear(void);
void SSD1306_SetCursor(uint8_t page, uint8_t column);
void SSD1306_DisplayOn(void);
void SSD1306_DisplayOff(void);

// Simple 5x7 font (only chars 32-127)
extern const uint8_t font5x7[96][6];

void SSD1306_PrintChar(char c);
void SSD1306_PrintString(const char *s);

void main(void) {
    // Initialize oscillator
    OSCCON = 0b01101010; // 8MHz

    // Initialize I2C
    I2C_Init();

    // Initialize SSD1306 OLED
    SSD1306_Init();

    // Display example text
    SSD1306_Clear();
    SSD1306_SetCursor(0, 0);
    SSD1306_PrintString("Erick, Eduardo, Ivan");

    while (1) {
        // Main loop
    }
}

// I2C routines
void I2C_Init(void) {
    TRISC3 = 1;   // SCL
    TRISC4 = 1;   // SDA
    SSPCON = 0b00101000;  // I2C Master mode
    SSPCON2 = 0;
    SSPADD = SSPADD_VAL;
    SSPSTAT = 0;
}

void I2C_Start(void) {
    SEN = 1;
    while (SEN);
}

void I2C_Stop(void) {
    PEN = 1;
    while (PEN);
}

bool I2C_Write(uint8_t data) {
    SSPBUF = data;
    while (BF);
    return !ACKSTAT; // 0 = ACK received
}

void SSD1306_Command(uint8_t cmd) {
    I2C_Start();
    I2C_Write(SSD1306_ADDR);
    I2C_Write(SSD1306_CMD);
    I2C_Write(cmd);
    I2C_Stop();
}

void SSD1306_Data(uint8_t data) {
    I2C_Start();
    I2C_Write(SSD1306_ADDR);
    I2C_Write(SSD1306_DATA);
    I2C_Write(data);
    I2C_Stop();
}

void SSD1306_Init(void) {
    __delay_ms(100);
    SSD1306_Command(0xAE); // Display off
    SSD1306_Command(0x20); // Set Memory Addressing Mode
    SSD1306_Command(0x00); // Horizontal addressing 
    SSD1306_Command(0xB0); // Page Start Address
    SSD1306_Command(0xC8); // COM Output Scan Direction
    SSD1306_Command(0x00); // Low column address
    SSD1306_Command(0x10); // High column address
    SSD1306_Command(0x40); // Start line address
    SSD1306_Command(0x81); // Contrast control
    SSD1306_Command(0x7F);
    SSD1306_Command(0xA1); // Segment re-map
    SSD1306_Command(0xA6); // Normal display
    SSD1306_Command(0xA8); // Multiplex ratio
    SSD1306_Command(0x3F);
    SSD1306_Command(0xA4); // Display RAM
    SSD1306_Command(0xD3); // Display offset
    SSD1306_Command(0x00);
    SSD1306_Command(0xD5); // Display clock
    SSD1306_Command(0x80);
    SSD1306_Command(0xD9); // Pre-charge period
    SSD1306_Command(0xF1);
    SSD1306_Command(0xDA); // COM pins
    SSD1306_Command(0x12);
    SSD1306_Command(0xDB); // VCOMH deselect level
    SSD1306_Command(0x40);
    SSD1306_Command(0x8D); // Charge pump
    SSD1306_Command(0x14);
    SSD1306_DisplayOn();
}

void SSD1306_DisplayOn(void) {
    SSD1306_Command(0xAF);
}

void SSD1306_DisplayOff(void) {
    SSD1306_Command(0xAE);
}

void SSD1306_Clear(void) {
    for (uint8_t page = 0; page < 8; page++) {
        SSD1306_Command(0xB0 | page);
        SSD1306_Command(0x00);
        SSD1306_Command(0x10);
        for (uint8_t col = 0; col < 128; col++) {
            SSD1306_Data(0x00);
        }
    }
}

void SSD1306_SetCursor(uint8_t page, uint8_t column) {
    SSD1306_Command(0xB0 | page);
    SSD1306_Command(0x00 | (column & 0x0F));
    SSD1306_Command(0x10 | ((column >> 4) & 0x0F));
}

void SSD1306_PrintChar(char c) {
    if (c < 32 || c > 127) return;
    for (uint8_t i = 0; i < 5; i++) {
        SSD1306_Data(font5x7[c - 32][i]);
    }
    SSD1306_Data(0x00);
}

void SSD1306_PrintString(const char *s) {
    while (*s) {
        SSD1306_PrintChar(*s++);
    }
}

